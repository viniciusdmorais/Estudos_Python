# Faça um programa que leia e valide as seguintes informações:
# Nome: maior que 3 caracteres;
# Idade: entre 0 e 150;
# Salário: maior que zero;
# Sexo: 'f' ou 'm';
# Estado Civil: 's', 'c', 'v', 'd';

def validainf(nome,idade,salario,sexo,estado_civil):

  while len(nome)<=3 or idade > 150 or idade < 0 or salario <= 0 or sexo not in(1,2) or estado_civil not in(1,2,3,4):
    if(len(nome)<=3):
      return(print("ERRO: Nome menor que 3 caracteres"))
    elif(idade > 150 or idade < 0):
      return(print("ERRO: idade precisa ser entre 0 e 150"))
    elif(salario < 0):
      return(print("ERRO: salário precisa ser maior que zero"))
    elif(sexo not in(1,2)):
      return(print("ERRO: sexo precisa ser masculino ou feminino"))
    elif(estado_civil not in(1,2,3,4)):
      return(print("ERRO: Estado Civil precisa ser \n1.Solteiro(a) \n2.Casado(a) \n3.Viúvo(a) \n4.Divorciado(a) "))  
    else:    
      return(print("Validação deu erro"))
      
  else:
    #Descricao estado civil
    if(estado_civil == 1):
      estcivil_desc = "Solteiro(a)"
    elif(estado_civil == 2):
      estcivil_desc = "Casado(a) "
    elif(estado_civil == 3):
      estcivil_desc = "Viúvo(a)"
    elif(estado_civil == 4):
      estcivil_desc = "Divorciado(a)"  
    #Descricao Sexo  
    if(sexo == 1):
      sexo_desc = "Masculino"
    else:
      sexo_desc = "Feminino"
    return(print("Nome:", nome,"\nIdade",idade,"\nSalário:",salario,"\nSexo:",sexo_desc,"\nEstado Civil:",estcivil_desc))

