def calcula_taxa_cresc(cresc_paisA,cresc_paisB,ano):
  while cresc_paisA <= cresc_paisB:
    cresc_paisA += cresc_paisA * 0.03
    cresc_paisB += cresc_paisB * 0.015
    ano = ano + 1
  else:
    return(print("Foi necessário ", ano," anos para passar."))

